// Number

// What
// - In JavaScript, the Number data type represents numeric values, including integers and floating-point numbers.

// Why
//- The Number data type is used to perform arithmetic calculations, comparisons, and manipulations on numeric values in JavaScript.
//- The Number data type provides a foundation for working with numbers in your program.

// How
// - The Number data type in JavaScript supports various operations, such as addition, subtraction, multiplication, division, and comparison.
// - JavaScript automatically converts numeric values to the Number data type when performing arithmetic operations.
// - The Number data type also includes special values, such as NaN (Not a Number) and Infinity, to represent invalid or infinite numeric values

// Syntax

// Number
let integer = 123;
let float = 3.14;
let scientificNumber = 10e2;

// BigInt: BigInt numbers can only perform operation with BigInt datatype

// Creating BigInt number using BigInt() constructor
let bigInt1 = BigInt(1000);

// Creating BigInt number using `n` at the end of the number
let bigInt2 = 1000n;

// NaN: represent invalid value
let notANumber = NaN;

// Infinity: represent  infinite numeric values
let infinity = Infinity;
